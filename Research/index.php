    This thesis examines emergent complexity and universal computation in spatio
    -temporal interactions of the iterated Prisonerâ€™s Dilemma Game (IPDG). Using
    a Cellular Automata simulation, cells hold different deterministic strategies 
    for playing the IPDG. Using these strategies, cells accumulate points. They then copy
    the strategies of neighboring cells whenever a neighboring cell makes more points. A 
    proof of holistic universal computation (Turing Machine equivalence) is shown, 
    followed by a discussion of the proof's greater implications. 
